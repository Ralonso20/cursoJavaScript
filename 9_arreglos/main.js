//METODOS POP/PUSH, SHIFT/UNSHIFT

let array = [];

array.push(1,2,3);

console.log(array);

array.pop(2);

console.log(array);

//agrega un elemento al principio
array.unshift(3);

console.log(array);

//Elimina un elemento al inicio
array.shift();

console.log(array);

